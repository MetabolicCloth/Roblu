package com.cpjd.roblu.tutorials;

class RTutorial {

    private final String title;
    private final String subtitle;

    RTutorial(String title, String subtitle) {
        this.title = title;
        this.subtitle = subtitle;
    }

    public String getTitle() {

        return title;
    }
    public String getSubtitle() {
        return subtitle;
    }

}
